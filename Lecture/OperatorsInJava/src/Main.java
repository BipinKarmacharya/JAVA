public class Main {
    public static void main(String[] args) {
        // Assignment operator "="
        int myInt = 9;
        System.out.println(myInt);

        // Arithmetic operator
        int a = 20;
        int b = 9;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);


        double x = 2.5;
        double y = 5.6;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);


        //Shorthand Operators
        int w = 5;
        int x1 = 7;

        w += x1;
        System.out.println("w + x1 =" + w);

    }
}